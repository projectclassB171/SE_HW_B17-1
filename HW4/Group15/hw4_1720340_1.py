#1. 编写程序，计算百钱买百鸡问题。假设公鸡5元一只，母鸡3元一只，小鸡1元三只，现在有100块钱，想买100只鸡，问有多少种买法？

r=0
for x in range(0,20):                      #假设公鸡数量为x只，其最大数量不得超过20只
   for y in range(0,33):                   #假设母鸡数量为y只，其最大数量不得超过33只
       for z in range(0,100):              #假设小鸡数量为z只，其最大数量不得超过100只
           if 5*x+3*y+z==100 and x+y+3*z==100:
               r=r+1
               print("公鸡：",x,"母鸡：",y,"小鸡：",z*3)
print("一共有：",r,"种买法。")
